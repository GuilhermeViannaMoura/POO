public class ConversaoDeUnidadesArea {
    public static double metro2ParaPe2(double x){
        return x * 10.76;
    }
    public static double pe2ParaCm2(double x){
        return x * 929;
    }
    public static double milha2ParaAcre(double x){
        return x * 640;
    }
    public static double acreParaPe2(double x){
        return x * 43560;
    }
    public static double metro2ParaAcre(double x){
        return x * 0.000247105;
    }
    public static double metro2ParaCm2(double x){
        return x * 10000;
    }
}
